const fs = require('fs')
const path = require('path')
const root = path.resolve(__dirname, '..')
for (const name of ['vue', 'vite', '@vitejs/plugin-vue', 'vue-tsc', 'element-plus']) {
  const p = path.join(root, 'node_modules', ...name.split('/'))
  try {
    const stat = fs.lstatSync(p)
    console.log('\n' + name)
    console.log('path=', p)
    console.log('isSymbolicLink=', stat.isSymbolicLink(), 'isFile=', stat.isFile(), 'isDirectory=', stat.isDirectory())
    if (stat.isSymbolicLink()) console.log('link->', fs.readlinkSync(p))
    if (stat.isFile()) console.log('content=', fs.readFileSync(p, 'utf8').slice(0, 300))
  } catch (e) {
    console.log('\n' + name, 'ERR', e.message)
  }
}
